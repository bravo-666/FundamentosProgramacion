'''
Realizar un algoritmos que lea un número y que muestre su raíz cuadrada y su
raíz cúbica. 

n1 == numero ingresado
raiz = raiz cuadrada
potencia = potencia al cuadrado
'''
n1 = float(input("Escribe el numero: "))

raiz = n1**(1/2)
potencia = n1**2 

print("La raiz cuadrada es: ",raiz)
print("La potencia al cuadrado es: ",potencia)
